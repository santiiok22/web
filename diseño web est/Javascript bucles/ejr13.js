function suma() {
    var num1 = parseInt(prompt("Ingrese un numero."));
    var num2 = parseInt(prompt("Ingrese el segundo numero."));
    var resu=num1+num2;
    document.write("El resultado es: "+resu);    
}
function resta() {
    var num1 = parseInt(prompt("ingrese un numero"));
    var num2 = parseInt(prompt("Ingrese el segundo numero"));
    var resu=num1-num2;
    document.write("El resultado es: "+resu);
}
